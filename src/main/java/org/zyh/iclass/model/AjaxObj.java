package org.zyh.iclass.model;

public class AjaxObj {

	private int id;
	private int status;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public AjaxObj() {
		this.id = 1;
		this.status = 1;
	}
	
	
}
